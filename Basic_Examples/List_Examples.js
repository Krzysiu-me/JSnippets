// Shows basic list (array) operations.
const fruits = ["apple", "banana", "cherry"];
fruits.push("orange"); // Add to end
console.log(fruits);
fruits.splice(1, 1); // Remove "banana"
console.log(fruits);
console.log(fruits.length); // List length
